static inline char EncodeCharRuc(char symbol)
{
 if(symbol == 0xA8)                             //Символ 'Ё'
  return 0xA2;
 else if(symbol == 0xB8)                        //Символ 'ё'
  return 0xB5;
 else if((symbol >= 0xC0) && (symbol <= 0xFF))	//Символы русского алфавита кроме 'ё' и 'Ё'
  return HD44780_table_code[symbol-0xC0];
 else                                           //Все остальные символы
  return symbol;
}

